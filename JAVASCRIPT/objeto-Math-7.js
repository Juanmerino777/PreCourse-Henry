//math.pow: Este nos permite potenciar un numero Ej: 2 x 2 x 2 = 8 El primer numero sera la base y el segundo la potencia.
Math.pow(2, 3); // = 8


//math.round, math.floor, math.ceil: Estos nos permiten redondear los numeros decimales Ej:(3.56).

math.round(0.49);// Redondea el numero decimal al entero mas proximo. Ej: (0.49 = 0) y (0.50 = 1). 
math.floor(5.93);// Redondea el numero decimal al entero de menor valor Ej: (5.93 = 5).
 
math.ceil(3.27); // Este redondea el decimal a el siguiente entero Ej: (3.27 = 4).

// math.max y math.min = Nos permiten conoser el maximo o minimo de un conjunto de numeros.
math.max(1,2,3,4,5);// Este es igual a 5.
math.min(1,2,3,4,5);// Este es igual a 1.

// math.random = Este genera un numero aleatorio decimal y puede ser entre el 0 y 1
math.random();// = 0.8051478163546508.
